package variable;

public class Var7 {
    public static void main(String[] args) { //변수(int, double --- 등) 타입,형식,형
        int a = 100; //정수를 a에 담음
        double b = 10.5; //실수
        boolean c = true; //불리언 true, false 입력가능
        char d = 'A'; //문자 하나 'A' '가'
        String e = "Hello Java"; //문자열, 문자열을 다루기 위한 특별한 타입

        //문자 고정된 값을 리터럴이라고 함. int a = 100 에서 100이 리터럴 -> 정수 리터럴
        //리터럴은 개발자가 고정한 값 리터럴 자체는 변하지 x

        System.out.println(a); //컨트롤 +d 바로 복사 가능
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
